# String Concatenation
str1 = "Hello, "
str2 = "world!"
result = str1 + str2
print("Concatenation Result:", result)

# String Slicing
text = "Python is great"
part = text[7:10]
print("Sliced Part:", part)

# String Length
length = len(text)
print("Length of String:", length)

# String Methods
sample_text = "   Python is awesome!   "
print("Original Text:", sample_text)

# Uppercase
uppercase_text = sample_text.upper()
print("Uppercase Text:", uppercase_text)

# Lowercase
lowercase_text = sample_text.lower()
print("Lowercase Text:", lowercase_text)

# Stripping Whitespace
stripped_text = sample_text.strip()
print("Stripped Text:", stripped_text)

# Replacing Substring
replaced_text = sample_text.replace("awesome", "fantastic")
print("Replaced Text:", replaced_text)

# Finding Substring
position = sample_text.find("Python")
print("Position of 'Python':", position)

# Splitting Text
split_words = sample_text.split()
print("Split Words:", split_words)
